        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register([Bind("FirstName,LastName,Email,Pin, ConfirmPin")] User user)
        {
            if (ModelState.IsValid)
            {
                await _userRepo.Register(user);
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        public IActionResult Login()
        {
            return View();
        }

        [HttpPost]
        public ActionResult Login(User userAccount)
        {
            var user =  _userRepo.Login(userAccount);
            if (user != null)
            {
                return RedirectToAction("Index", "Home");
            }

            return View(user);
        }

