        public UserController(IUserRepository repo) //need to reference the interface or else we'll get a 'service not found' exception
        {
            this._userRepo = repo;
        }

        // GET: User
        public async Task<IActionResult> Index()
        {
            return View(await _userRepo.Get());
        }

        // GET: User/Details/5
        public async Task<IActionResult> Details(string id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _userRepo.Get(id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // GET: User/Create
        public IActionResult Register()
